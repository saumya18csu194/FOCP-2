import java.util.*;
class integer{
		public static void main(String[] args) {
    	int num1 = 20;
    	short num2 = 10;
    	int sum = num1 + num2;
    	int difference = num1 - num2;
    	long product = num1 * num2;
    	float quotient = num1 / num2;
    	int remainder = num1 % num2;
    	System.out.println("Number are : " + num1 + " and " + num2);
		System.out.println("Sum \t:\t" + sum);
		System.out.println("Difference \t:\t" + difference);
		System.out.println("Product \t:\t" + product);
		System.out.println("Quotient \t:\t" + quotient);
		System.out.println("Remainder \t:\t" + remainder);
    }
}
